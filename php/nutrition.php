		<h1 style="font-size:1.5em;font-weight: bold;text-align:center;"><u>My Nutrition Philosophy</u></h1>
		<p style="padding-top:10px;">
		  As a health and fitness enthusiast, I've spent a lot of time doing personal research (and experiments!) in the exploration of proper nutrition.
		  I've tried going <a href="https://en.wikipedia.org/wiki/Paleolithic_diet" target="_blank">paleo</a>, I've tried
		  <a href="https://en.wikipedia.org/wiki/Intermittent_fasting" target="_blank">intermittent fasting</a> (IF), I've counted calories, macros, micros, and even experimentally determined
		  my <a href="https://en.wikipedia.org/wiki/Basal_metabolic_rate" target="_blank">basal metabolic rate</a> (BMR) (see here). Through these experiences I've accumulated some knowledge
		  and constructed some resources that I'd like to share with the world, and here's where I'll do it!
		</p>
		<h2 style="font-size:1.2em;font-weight: bold;padding-top:30px;"><u>Meal Prep</u></h2>
		<p style="padding-top:10px;">
		  First off, I'm a huge believer in meal preparation ('meal prep'): cooking many servings of food at once. The <i>efficiency</i> is just unparalleled!
		  To me, the process of planning out a meal, actually cooking everything, and then washing the dishes is not that quick. The thought of repeating this process
		  every day for every meal is <strong>repulsive</strong>. I acknowledge that some people thoroughly enjoy the process or find it relaxing, and I actually do like
		  cooking, but not as much as other things. If one were to plan 1 or 2 meals different meals out and simply cook, say, 5 servings of each at one time, then there is a large amount
		  of time saved in not having to 'set up' and 'clean up' for each of the 5-10 meals you now have -- you only do this once. It may take a little extra longer to cook a
		  larger amount of food, but the gains outweigh the losses here.
		</p>
		<p style="padding-top:20px;">
		  Not only does meal prep save time, but it also saves money. During my time in graduate school, my budget was really tight. I was living
		  on my own, supporting myself and my child, and still trying to live a little here and there. I found that meal prep was one way to really stay on top of my food costs
		  since I could just buy everything in bulk.
		</p>
		<p style="padding-top:20px;">
		  Last but not least, meal prep is the way to go for achieving fitness and proper nutrition. Anyone who has been focused on their personal health or making gains in the gym
		  knows that the balance between exercise and proper nutrition weighs heavier on the nutrition side. Personally, I believe most fitness goals are satisfied with an effort that is spread with 70% on nutrition and 30% on exercise.
		  If you're a 70kg male lifting heavy in the gym for 2 hours per day and 5 days per week, but only ingest 60g of protein per day and you get your calories from iced cappucinnos,
		  you're losing out on gains. Meal prep allows for easier tracking and control of calories and macronutrients because you are generally using a limited amount of 
		  ingredients and can easily divide the total nutritional content among your the number of servings you create. Additionally, how many times have you made an unhealthy meal choice
		  due to <i>convenience</i>? If you have healthy choices immediately available to you, it's easier to make a healthy choice.
		</p>
		<p style="padding-top:20px;">
		  So, in comes meal prep to save the day. I have compiled a list of 20+ homemade recipes that are low cost, nutritionally dense, high in protein, and are easy to make.
          I want to stress that last part: most of these are SIMPLE. Meal prep does not need to be complicated. Buy some chicken breasts, a can of beans, a can of vegetable soup, throw it all together with
		  some leftover rice and BOOM: you've got a serious meal (roughly 550 calories, 47 g carbs, 70 g protein, 9 g fat, 10 g sugar depending on the relative proportions. Don't worry, the recipes below definitely
          specify what the relative proportions should be! )
		  Popular ingredients are fish, chicken, beef, tofu, rice and/or quinoa, frozen
		  vegetables, and sweet potatoes. Although not necessary, a good spice collection and sauces go a long way. One absolute necessity is tupperware! Hopefully you find at least a few recipes you could get used to.
		  
		</p>
		
